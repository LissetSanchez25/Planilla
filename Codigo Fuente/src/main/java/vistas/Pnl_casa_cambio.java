    public void guardarEnBD(double s, double d, double m, double total) {
        ConexionBD con = new ConexionBD();
        String sql = "INSERT INTO historial_operaciones (monto_soles, cambio_dolar, cambio_marco, total_euros) VALUES (?, ?, ?, ?)";

        try (java.sql.Connection cn = con.conectar();
             java.sql.PreparedStatement ps = cn.prepareStatement(sql)) {

            ps.setDouble(1, s);
            ps.setDouble(2, d);
            ps.setDouble(3, m);
            ps.setDouble(4, total);

            ps.executeUpdate(); // Ejecuta la inserción
            System.out.println("Registro guardado en el historial con éxito.");

        } catch (java.sql.SQLException e) {
            System.err.println("Error al guardar: " + e.getMessage());
        }
    }
    
    public void consultarHistorial() {
        ConexionBD con = new ConexionBD();
        String sql = "SELECT * FROM historial_operaciones ORDER BY fecha_registro DESC";

        // Definimos las columnas de la tabla
        String[] columnas = {"ID", "Soles", "Dólar", "Marco", "Total Euros", "Fecha"};
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(null, columnas);

        try (java.sql.Connection cn = con.conectar();
             java.sql.Statement st = cn.createStatement();
             java.sql.ResultSet rs = st.executeQuery(sql)) {

            String[] fila = new String[6];
            while (rs.next()) {
                fila[0] = rs.getString("id");
                fila[1] = rs.getString("monto_soles");
                fila[2] = rs.getString("cambio_dolar");
                fila[3] = rs.getString("cambio_marco");
                fila[4] = rs.getString("total_euros");
                fila[5] = rs.getString("fecha_registro");
                modelo.addRow(fila);
            }
            tblHistorial.setModel(modelo); // Asignamos los datos a tu JTable

        } catch (java.sql.SQLException e) {
            System.err.println("Error al consultar historial: " + e.getMessage());
        }
    }

