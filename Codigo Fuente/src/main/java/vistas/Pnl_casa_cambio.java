    private void jMostrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMostrarBtnActionPerformed
        // TODO add your handling code here:
        if (evt.getSource()==jMostrarBtn) {
            
            double soles = Double.parseDouble(jSolesField.getText());
            double dolares = Double.parseDouble(jDolaresField.getText());
            double marcos = Double.parseDouble(jMarcosField.getText());
            
            double montoTotal = (soles / dolares + soles / marcos) * 1.09;
            
            String resultado = String.format("%.2f", montoTotal);
            jResumenArea.setText(" ** RESUMEN DE CASA DE CAMBIO ** ");
            jResumenArea.append("\n");
            jResumenArea.append("\nEL MONTO TOTAL EN EUROS ES: " + resultado);
            
            // 4. GUARDAR: Enviar a la base de datos (Lo haremos en el siguiente paso)
            guardarEnBD(soles, dolares, marcos, montoTotal);
            //  PASO NUEVO: Refresca la tabla para ver el nuevo registro inmediatamente
            consultarHistorial();
        }
    }//GEN-LAST:event_jMostrarBtnActionPerformed

