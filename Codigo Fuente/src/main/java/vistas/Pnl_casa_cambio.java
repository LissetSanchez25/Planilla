    public void sincronizarPrecios() {
    try {
        // 1. Conectamos usando tu clase
        ConexionBD con = new ConexionBD();
        // 2. Pedimos los precios (Asegúrate de haber creado la tabla en phpMyAdmin)
        try (Connection cn = con.conectar()) {
            // 2. Pedimos los precios (Asegúrate de haber creado la tabla en phpMyAdmin)
            String sql = "SELECT moneda, precio FROM tipos_cambio";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            // 3. Llenamos los campos automáticamente
            while (rs.next()) {
                String moneda = rs.getString("moneda");
                String precio = rs.getString("precio");
                
                if (moneda.equalsIgnoreCase("Dolar")) {
                    jDolaresField.setText(precio);
                }
                if (moneda.equalsIgnoreCase("Marco")) {
                    jMarcosField.setText(precio);
                }
            }
        }
    } catch (SQLException e) {
        System.err.println("Error de sincronización: " + e.getMessage());
    }
    }
    
