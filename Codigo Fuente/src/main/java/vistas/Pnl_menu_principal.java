    private void mostrarBienvenida() {
        LocalDateTime ahora = LocalDateTime.now();

        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy hh:mm a");

        String mensaje = "¡Bienvenido, Usuario" + " | " + ahora.format(formato);

        jTituloLabel.setText(mensaje);
    }
    
    private void jPagoEmpleadosBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPagoEmpleadosBtnActionPerformed
        // Si el panel aún no ha sido agregado, lo agregamos una sola vez
        if (pnl_vista_principal.getComponentCount() == 0 || 
            !java.util.Arrays.asList(pnl_vista_principal.getComponents()).contains(pnl_pago_empleados)) {

            pnl_vista_principal.add(pnl_pago_empleados, "Pago de Empleados");
        }

        vista.show(pnl_vista_principal, "Pago de Empleados");

        // Forzar revalidación y redibujado
        pnl_vista_principal.repaint();

        // Opcional: ajustar el JFrame
        this.pack();
    }//GEN-LAST:event_jPagoEmpleadosBtnActionPerformed

