        void verificarConexion() {

        try {
            ConexionBD cn = new ConexionBD();
            Connection con = cn.conectar();

            if (con != null && !con.isClosed()) {

                jConexionLabel.setText("Conectado");

                jConexionEstadoLogo.setIcon(
                    new ImageIcon(getClass().getResource("/icons/bd_on.png"))
                );

                con.close();
            }

        } catch (Exception e) {

            jConexionLabel.setText("Sin conexión");

            jConexionEstadoLogo.setIcon(
                new ImageIcon(getClass().getResource("/icons/bd_off.png"))
            );
        }
    }

